/*---------------------------------------------------------------*/
/**
keyの初期化
*/
/*---------------------------------------------------------------*/
void Key::Init(Color side, const Board& board, const Hand& hand)
{
	uint64_t key = 0;

	ASSERT(side != NO_COLOR);

	for (int i = 0; i < SQUARE_NB; i++)
	{
		Piece piece = board[i];
		key = key ^ Key::zobBoard[piece][i];
	}

	this->boardkey_ = key;

	uint32_t hand_b = 0;
	uint32_t hand_w = 0;

	for (int i = PAWN; i < PIECE_TYPE_NB; i++)
	{
		hand_b |= Key::handTable[i] * hand[BLACK][i];
		hand_w |= Key::handTable[i] * hand[WHITE][i];
	}

	this->hand_b_ = hand_b | side;
	this->hand_w_ = hand_w;
}


